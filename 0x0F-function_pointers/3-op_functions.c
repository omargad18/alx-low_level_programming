#include "3-clac.h"

/**
 * op_add - functions adds two integers
 * @a: first int
 * @b: second int
 *
 * Return: the sum
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function subtracts two integers
 * @a: first int
 * @b: second int
 *
 * Return: the difference
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_div - function divides two integers
 * @a: first int
 * @b: second int
 *
 * Return: the result of division
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function modulus two integers
 * @a: first int
 * @b: second int
 *
 * Return: remainder
*/

int op_mod(int a, int b)
{
	return (a % b);
}

